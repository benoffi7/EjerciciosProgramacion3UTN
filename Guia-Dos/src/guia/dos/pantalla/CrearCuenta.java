    private void jButtonAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAceptarMouseClicked

        boolean correcto = false;

        if (jRadioButtonCtaVacia.isSelected()) {

            cuenta = new Cuenta();
            correcto = true;

        } else if (Float.parseFloat(jTextFieldMontoInic.getText()) <= 0) {

            JOptionPane.showMessageDialog(null, "Error con el monto", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else {

            cuenta = new Cuenta(Float.parseFloat(jTextFieldMontoInic.getText()));
            correcto = true;

        }

        if (correcto) {
            JOptionPane.showMessageDialog(null, "Cuenta creada con éxito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            Operacion marco = new Operacion();
            this.setVisible(false);
            marco.setVisible(true);
        }
    }//GEN-LAST:event_jButtonAceptarMouseClicked

