    protected static void resaltarBotones(JButton boton, Color color) {
        boton.setBackground(color);
    }

    protected static void desresaltarBotones(JButton boton, Color color) {
        boton.setBackground(color);
    }

    protected static void limpiarJtextField(JTextField jt, String textoInicial){
        if(jt.getText().equals(textoInicial)){
            jt.setText(" ");
        }
    }
    
    protected static void mostrarLabelAviso(JTextField jt, JLabel error, JLabel correcto, JLabel textoError){
        
        if ((Float.parseFloat(jt.getText()) <= 0) || (Float.parseFloat(jt.getText())%10 != 0)) {
            correcto.setVisible(false);
            textoError.setVisible(true);
            error.setVisible(true);
        } else {
            correcto.setVisible(true);
            textoError.setVisible(false);
            error.setVisible(false);
        }
    }
    
